var visor1Trigger = document.getElementsByClassName('animatedHelmetVisor1');
for (let element of visor1Trigger) {
  element.onmouseenter = function () {
    document.getElementById('visor1').style.clip="rect(0px,145px,0px,0px)";
  }
  element.onmouseleave = function () {
    document.getElementById('visor1').style.clip="rect(0px,1000px,200px,0px)";
  }
}

